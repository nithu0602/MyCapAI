#Python Program to Illustrate Different Set Operations

E = {0, 2, 4, 6, 8}   
N = {1, 2, 3, 4, 5}

print("Union of E and N: ",E | N)
print("Intersection of E and N: ",E&N)
print("Difference of E and N is: ",E-N)
print("Symmetric difference of E and N: ",E^N)